#include <bits/stdc++.h>
using namespace std;

int main() {
    //this controls outer loops like rows or u can say like going for next line, we can do similar
    for (int i = 0; i < 5; i++) {

        //this controls inner loops like columns or u can say like printing stars in a line
        for (int j = 0; j < 5; j++) {
            cout << "*";
        }
        cout << endl;
    }
    return 0;
}
